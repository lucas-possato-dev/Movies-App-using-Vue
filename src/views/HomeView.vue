<script>
import { ref } from "vue";
import env from "../env";

export default {
  setup() {
    const search = ref("");
    const movies = ref([]);
    const loading = ref(false);

    const SearchMovies = () => {
      if (search.value != "") {
        loading.value = true;
        fetch(`https://www.omdbapi.com/?apikey=${env.apikey}&s=${search.value}`)
          .then((response) => response.json())
          .then((data) => {
            movies.value = data.Search;
            search.value = "";
          })
          .finally(() => {
            setTimeout(() => {
              loading.value = false;
            }, 2000);
          });
      }
    };

    return {
      search,
      movies,
      SearchMovies,
      loading,
    };
  },
};
</script>

